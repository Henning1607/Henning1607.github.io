                <h3>Hva bør jeg unngå?</h3>

                <p>
                    Ikke bruk vanlige erstatninger som å bytte bokstaver med lignende tegn og tall, for eksempel «s» og «$».
                    Ikke velg en setning som åpenbart er knyttet til siden man er på, som f.eks. «Jeg liker Digipost» (selv om det er hyggelig).
                    Ikke forsøk «smarte» løsninger som å velge taster ved siden av hverandre på tastaturet. «********», «passord», «qwertyui» og «1234567890» er veldig vanlige, men dårlige passord.</p>

