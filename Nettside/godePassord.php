                <h3>Hvordan lager jeg et slikt passord?</h3>
                <p>For å lage et godt passord kan man gjerne ta utgangspunkt i en setning. Den bør helst ha fire eller flere ord. For å gjøre det vanskeligere å gjette kan man gjøre en utskifting av enkelte bokstaver. Man kan for eksempel erstatte alle mellomrom med «a», eller alle «e»-er med «k». Lag deg <em>dine egne regler</em> her. Du bør unngå å erstatte med tegn som ligner, for eksempel erstatte «E» med «3» eller «a» med «@».</p>

                <ul>
                    <li>Bruk tall og spesialtegn i passordet</li>
                    <li>Bruk gjerne en setning med norske bokstaver</li>
                </ul>

                <p>La oss ta et enkelt eksempel. Jeg finner meg en setning: «Jeg så en knallbra film i går». Deretter erstatter jeg alle mellomrom med «a». Passordet blir da «Jegasåaenaknallbraafilmaiagår». Dette passordet er på hele 29 bokstaver, men er allikevel enkelt å huske. Jeg trenger nemlig bare å huske setningen og reglene jeg bruker for å erstatte tegn. Dette er betydelig lettere å huske enn kompliserte passord som «aK2+k3nN», og allikevel mye vanskeligere å gjette i de fleste tilfeller.</p>

