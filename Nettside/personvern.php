                <h2>Personvern i Digipost</h2>

<p>For Digipost er ditt personvern og din datasikkerhet det aller viktigste, og vi vil alltid behandle dine personopplysninger:</p>
<ul>
   <li>med konfidensialitet
   </li><li>på riktig måte, i henhold til egne erklæringer
   </li><li>på lovlig vis, i samsvar med gjeldende lover og forskrifter om personvern og informasjonssikkerhet
</li></ul>

<p>Dokumenter som du har mottatt eller lastet opp til Digipost er din private informasjon, og Digipost vil innenfor lovens rammer bare behandle denne informasjonen når du selv som har initiert behandlingen.</p>

<h3>Behandlingsansvarlig</h3>

<p>Posten vil behandle personopplysninger som følge av at brukeren benytter Digipost og vil ved all behandling av personopplysninger følge bestemmelsene i personopplysningsloven av 14. april 2000 nr. 31. Behandlingsansvarlig er Posten Norge AS, Postboks 1500 Sentrum, 0001 Oslo.</p>

<h3>Formålet med behandlingen av opplysningene</h3>

<p>Formålet med behandling av personopplysninger i Digipost er å oppfylle de rettigheter og plikter som følger av <a href="/juridisk">avtalevilkårene</a> for Digipost. Formålet er videre å administrere og videreutvikle tjenestene, å gjennomføre autentiseringsprosesser, diagnostisere problemer i Digipost og tilhørende systemer, å utvikle nye ideer og tjenester, samt å kommunisere med brukeren. Personopplysninger vil også kunne bli benyttet til å rette markedsføringshenvendelser til brukeren i henhold til <a href="/juridisk">avtalevilkårene</a> og personopplysningslovens og markedsføringslovens bestemmelser.</p>

<h3>Opplysningskategorier som behandles</h3>

<p>Ved opprettelse av bruker/konto i Digipost og ved videre bruk av Digipost samles det inn informasjon om brukeren. Opplysningskategoriene er forklart nærmere under, og gjelder generell kontakt- og bruksinformasjon samt statistikk om bruken av tjenesten. For å kunne tilby tjenestene på Digipost behandles også informasjon knyttet til cookies, IP-adresser og brukerens fødselsnummer.</p>

<h4>Kontakt- og bruksinformasjon</h4>

<p>Følgende opplysninger samles inn i forbindelse med registrering og bruk av Digipost:</p>

<ul>
   <li>Navn
   </li><li>Kontaktinformasjon: Adresse, telefonnummer, e-postadresse
   </li><li>Organisasjons- og fødselsnummer
   </li><li>Profilinformasjon: Alder, kjønn og annen informasjon brukeren velger å gjøre tilgjengelig
   </li><li>Elektronisk identifikasjon (IP-adresse, cookies)
   </li><li>Informasjon om brukerens bruk av Digipost-løsningen
   </li><li>Trafikkdata
   </li><li>Adresseliste
   </li><li>Kredittkort- og annen betalingsinformasjon i tilfeller hvor dette er nødvendig
</li></ul>

<h4>Statistikk om bruken av tjenestene</h4>

<p>Ved bruk av Digipost samles det også inn opplysninger som ikke kan knyttes til brukeren og som kun benyttes til utarbeiding av generell statistikk, samt til forbedring av tjenestene. Dette er informasjon om:</p>

<ul>
   <li>Nettleser (browser) og innstillinger på denne
   </li><li>Operativsystem
   </li><li>Skjermoppløsning og antall farger på skjermen
   </li><li>Internettforbindelse
   </li><li>Besøkte sider på Digipost og tidspunkt for besøk
   </li><li>Hvilken side brukeren eller kunder kommer til Digipost fra
</li></ul>

<h4>Cookies og IP-adresser</h4>

<p>Digipost benytter cookies for innsamling av statistikk. Cookies er små tekstfiler som lagres i brukerutstyret i forbindelse med bruk av internettjenester. Det foretas ikke identifisering av den enkelte bruker som person i forbindelse med anvendelsen av cookies. Brukeren kan ved å endre innstillingene i sin nettleser velge å avstå fra bruk av cookies. For å logge på og benytte Digipost er det derimot en forutsetning at cookies aksepteres. Digipost vil også kunne benytte tilsvarende teknologi som cookies, som ”web-beacons”, ”pixel tags” mv. samt annen teknologi som vil dekke samme funksjoner som cookies i fremtiden.</p>

<p>Digipost vil også kunne lagre brukerens IP-adresse. En IP-adresse er et unikt nummer som tildeles de nettverksenhetene, f.eks. en datamaskin, som kommuniserer med hverandre over Internett. Brukerens IP-adresse vil kun bli benyttet til statistiske formål og til innsamling av demografisk informasjon i vid forstand, og vil derfor ikke kunne knyttes til brukeren eller kunden.</p>

<h4>Bruk av fødselsnummer</h4>

<p>For at brukeren skal kunne logge seg på sin personlige Digipost-konto, må brukeren benytte sitt fødselsnummer (11 siffer) samt et selvvalgt passord. For å få tilgang til meddelelser hvor avsender har stilt høyere krav til sikkerhet må brukeren logge på med sitt fødselsnummer og sin valgte autentiseringsløsning (Bank ID utstedt av hvilken som helst bank i Norge, eller annet tilsvarende sikkerhetsinstrument godkjent av Posten).</p>

<p>Digipost registrerer og oppbevarer brukerens fødselsnummer i forbindelse med opprettelsen av en Digipost-konto. Brukerens fødselsnummer behandles utelukkende for autentisering av brukeren og for å sikre at brukeren er rett mottaker (identifisering) av meddelelser fra offentlige myndigheter, virksomheter og organisasjoner som har tillatelse til å sende meddelelser knyttet opp mot fødselsnummer som identifiseringsnøkkel.</p>

<p>Brukerens fødselsnummer vil ikke videreformidles, utleveres eller anvendes til andre formål enn Digipost med mindre den offentlige myndigheten, virksomheten eller organisasjonen har tillatelse til dette på annet grunnlag. Fødselsnummer videresendes elektronisk i de tilfeller hvor en via en link i Digipost kan komme til en annen nettside og automatisk logges på.</p>

<p>Digipost benytter Posten Norge AS’ adresseregister for å kunne koble brukeren opp mot en fysisk adresse. Endrer brukeren adresse hos Posten Norge AS, vil den fysiske adressen brukeren knyttes opp mot i Digipost automatisk oppdateres.</p>

<h3>Utlevering til tredjepart</h3>

<p>Dersom det er en forutsetning for å sende, motta eller arkivere meddelelser, eller utføre andre tjenester i Digipost, vil personopplysninger som er nødvendig for å utføre slik tjeneste bli utlevert til tredjepart.</p>

<p>Med unntak som følger av <a href="https://www.digipost.no/juridisk">avtalevilkårene</a>, vil ikke personopplysninger bli utlevert til tredjepart for kommersiell bruk. Posten deler informasjon som ikke er identifiserbar på personnivå med tredjeparter for å hjelpe til å bedre kunne forstå bruksmønster for ulike typer meddelelser, tilbud, annonsering og andre tjenester/funksjonalitet knyttet til Digipost.</p>

<h3>Rett til innsyn, retting og sletting</h3>

<p>Etter personopplysningsloven har den enkelte rett til innsyn i egne personopplysninger. Dersom opplysninger på Digipost er feil, vil det være adgang til å få de rettet eller slettet. Alle opplysninger som er hentet fra brukeren, vil være tilgjengelige for brukeren via digipost-kontoen, og kan rettes av brukeren via Digipost-kontoen eller Postens tjeneste for adresseendring. Det tas forbehold om at enkelte endringer kan kreve godkjenning før de aktiveres i Digipost.</p>

<h3>Informasjonssikkerhet</h3>

<p>Digipost har som mål å være den sikreste internettløsningen i Norge, og benytter anerkjente standarder og beste praksis i arbeidet med sikkerhet. Digipost gjennomfører jevnlig risikovurderinger og sikkerhetstester av løsningen, og rådfører seg også med Datatilsynet ved hver endring i løsningen som kan berøre personvernet.</p>

<p>Digipost har implementert følgende sikkerhetstiltak i løsningen (ikke uttømmende):</p>

<ul>
   <li>Autentiseringsløsning på sikkerhetsnivå 4.
   </li><li>All informasjonsutveksling skjer med sterk kryptering (SSL, SSH).
   </li><li>Sensitiv informasjon kan meldingskrypteres ende-til-ende med kvalifiserte sertifikater.
   </li><li>Alle handlinger som utføres av driftspersonell logges i egne hendelsesregistre.
   </li><li>Alle dokumenter ligger lagret kryptert, med sterke krypteringsnøkler.
   </li><li>Alle avsendere og mottakere blir identifisert etter strenge retningslinjer. Dette gjør at du alltid kan være sikker på hvilken avsender en meddelelse er sendt fra.
   </li><li>Digipost tjenere er plassert i et av Norges sikreste driftssentre, hos en leverandør som har lang erfaring med å håndtere virksomhetskritisk og sensitiv informasjon.
   </li><li>Det blir tatt sikkerhetskopi av alle dine dokumenter, og alle dokumenter blir lagret på to forskjellige steder til enhver tid slik at du er beskyttet mot brann, ødeleggelse av tjenere osv.
   </li><li>Digipost-ansatte har ikke tilgang til dine dokumenter. Det er kún et fåtall driftspersoner som har tilgang til Digipost sine systemer, men siden alle dokumenter ligger lagret kryptert, så har heller ikke disse innsyn i dine data.
</li></ul>


<h3>Personvern i Digipost sine app-er</h3>

<p>Digipost samler ikke inn brukerdata fra app-ene for iOS og Android. App-ene aksesserer således ingen data på brukerens enhet utover de data som ligger i app-en og som hentes via Digipost sin nettjeneste. I dette avsnittet utdypers vanlige spørsmål om hvordan Digipost sine app-er fungerer og hvilke data som aksesseres.</p>

<h4>Hva blir samlet inn av informasjon?</h4>

<p>App-en lagrer brukerens navn og passord som brukeren har registrert seg med i Digipost, inntil brukeren velger å logge ut. App-en laster ned brev, kvitteringer og dokumenter fra Digipost sine servere.</p>

<h4>Hvor og hvordan blir informasjon lagret?</h4>

<p>App-en lagrer all informasjonen kryptert med AES256-kryptering med nøkler lagret i iOS Keychain.</p>

<h4>Hvor lenge blir informasjonen lagret?</h4>

<p>Helt til brukeren sletter appen.</p>

<h4>Hvilke rutiner finnes for sletting av informasjon?</h4>

<p>Brukeren kan selv velge å deaktivere sin konto i Digipost. Tre måneder etter deaktivering slettes all informasjon fra Digipost sine servere og vil ikke lenger være tilgjengelig i app-en.</p>

<h4>Hva brukes informasjonen til (viderebruk)?</h4>

<p>Ingen viderebruk. All data ligger lukket (sandbox-et) i app-en, og er utilgjengelig for andre apper.</p>

<h4>Knyttes informasjonen til person eller blir den anonymisert?</h4>

<p>All informasjon knyttes til den registrerte Digipost-brukeren.</p>

<h4>Får brukeren beskjed om hva slags informasjon som blir innhentet, samt formålet?</h4>

<p>Ja, gjennom denne personvernerklæringen.</p>

<h4>Må brukeren akseptere vilkår for å installere App-en?</h4>

<p>Det er ingen vilkår for å innstallere app-en, men app-en fungerer kun for registrerte brukere og følger de generelle brukervilkårene for Digipost.</p>

<h4>Annen logging og innsending av informasjon</h4>

<p>Det logges kall mot Digipost sine servere, hvilket er normalt for alle web-tjenester. Disse loggene oppbevares i ca. to måneder, før de slettes.</p>

<p>Dersom app-en krasjer vil brukerne bli spurt om de ønsker å sende informasjon om dette til oss. Da sendes aggregerte krasjlogger. Disse loggene er anonymiserte og benyttes kun til feilsøking.</p>


        


